		<h2>Warnings</h2>
		<p>
			Any member of conference staff can issue a verbal warning to a participant that their
			behaviour violates the conference's anti-harassment policy. Warnings should be reported to
			conference committee members as soon as practical. The report should include:
		</p>
		<ul>
			<li>Identifying information (name/badge number) of the participant</li>
			<li>The time you issued the warning</li>
			<li>The behaviour that was in violation</li>
			<li>The approximate time of the behaviour (if different than the time of warning)</li>
			<li>The circumstances surrounding the incident</li>
			<li>Your identity</li>
			<li>Other people involved in the incident</li>
		</ul>
		<h2>Presentations</h2>
		<p>
			Presentations or similar events should not be stopped for one-time gaffes or minor problems,
			although a member of conference staff should speak to the presenter afterward. However, staff
			should take immediate action to politely and calmly stop any presentation or event that
			repeatedly or seriously violates the anti-harassment policy. For example, simply say "I'm
			sorry, this presentation cannot be continued at the present time" with no further explanation.
		</p>
		<h2>Taking reports</h2>
		<p>
			When taking a report from someone experiencing harassment you should record what they say and
			reassure them they are being taken seriously, but avoid making specific promises about what
			actions the organizers will take. Ask for any other information if the reporter has not
			volunteered it (such as time, place) but do not pressure them to provide it if they are
			reluctant. Even if the report lacks important details such as the identity of the person
			taking the harassing actions, it should still be recorded and passed along to the appropriate
			staff member(s). If the reporter desires it, arrange for an escort by conference staff or a
			trusted person, contact a friend, and contact local law enforcement. Do not pressure the
			reporter to take any action if they do not want to do it. Respect the reporter's privacy by
			not sharing unnecessary details with others, especially individuals who were not involved with
			the situation or non-staff members.
		</p>
		<h2>Expulsion</h2>
		<p>
			A participant may be expelled by the decision of any of the above listed entities for whatever
			reasons they deem sufficient. However, here are some general guidelines for when a participant
			should be expelled:
		</p>
		<ul>
			<li>A second offence resulting in a warning from staff</li>
			<li>Continuing to harass after any "No" or "Stop" instruction</li>
			<li>A pattern of harassing behaviour, with or without warnings</li>
			<li>A single serious offence (e.g., punching or groping someone, taking up-skirt photos)</li>
		</ul>
		<h2>Public statements</h2>
		<p>
			As a general rule, conference staff should not make any public statements about the behaviour
			of individual people during or after the conference. In general, consult with other staff
			members when possible but act when necessary.
		</p>
